function Get-RelativePath {
    param([string]$Path)
    return $Path.Substring($Workspace.Length + 1).Replace("\", "/")
}

function Get-RepoFiles {
    Get-ChildItem -Path $Workspace -Recurse -Force -File | Where-Object {
        $_.FullName -notmatch '\\\.git\\' -and
        $_.FullName -notmatch '\\node_modules\\' -and
        $_.FullName -notmatch '\\dist\\' -and
        $_.FullName -notmatch '\\build\\'
    }
}

function Ensure-Log {
    if (-not (Test-Path $LogPath)) {
        Set-Content -Path $LogPath -Encoding UTF8 -Value "| Task ID | Description | Source Files Analyzed | Target Path | Status |`n|---|---|---|---|---|"
        return
    }
    $raw = Get-Content $LogPath -Raw
    if ($raw -notmatch '\| Task ID \| Description \| Source Files Analyzed \| Target Path \| Status \|') {
        $archive = $raw
        Set-Content -Path $LogPath -Encoding UTF8 -Value "| Task ID | Description | Source Files Analyzed | Target Path | Status |`n|---|---|---|---|---|`n`n## Archived Previous Log Format`n`n$archive"
    }
}

function Test-LoggedDone {
    param([string]$TaskId)
    if (-not (Test-Path $LogPath)) { return $false }
    $raw = Get-Content $LogPath -Raw
    return $raw -match "\|\s*$([regex]::Escape($TaskId))\s*\|[^\r\n]*\|\s*DONE\s*\|"
}

function Write-TaskLog {
    param([string]$TaskId, [string]$Description, [string]$Sources, [string]$TargetPath, [string]$Status)
    Add-Content -Path $LogPath -Encoding UTF8 -Value "| $TaskId | $Description | $Sources | $TargetPath | $Status |"
}

function Get-SourceSummary {
    param([string[]]$Paths)
    $highSignal = $Paths | Where-Object {
        $_ -match '^(AGENTS|AI_EXECUTION_PROMPTS|CLAUDE|LOCAL_EXECUTION_MODE|README)\.md$' -or
        $_ -match '^governance/' -or
        $_ -match '(^|/)continue/rules/' -or
        $_ -match '(^|/)cursor/rules/' -or
        $_ -match '(^|/)github/' -or
        $_ -match '^Source/(\.cursor|Changelogs|Codinglessonslearnt|own prompts|prompt-template|prompts/next-shadcn|prompts/react-19|prompts/tailwind-4|scripts)/'
    }
    return "Full repository scan captured in AI_PROMPTING_FOLDERSTRUCTURE/docs/repository_source_inventory.md; high-signal sources: " + (($highSignal | Sort-Object) -join "; ")
}

