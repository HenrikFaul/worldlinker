        if (-not (Test-LoggedDone $TaskId)) {
            Write-TaskLog $TaskId "$Description already present" $Sources $RelativePath "DONE"
        }
        return
    }
    Add-Content -Path $target -Encoding UTF8 -Value (New-EnhancementBlock $Domain $Kind)
    Write-TaskLog $TaskId $Description $Sources $RelativePath "DONE"
}

